<h3>What does each tool do?</h3>
<i class="fa fa-files-o fa-lg" aria-hidden="true"></i> <b>Files</b>
<ul>
	<li>Lists all files added, modified and removed starting from WoW patch 6.0 and up (older versions are planned to be added)</li>
	<li>Download different versions of a file (when logged in)</li>
	<li>Many filters to better find the files you are looking for</li>
</ul>
<i class="fa fa-table fa-lg" aria-hidden="true"></i> <b>DBCs</b>
<ul>
	<li>Browse WoW's database files</li>
	<li>Search for specific values</li>
	<li>Export data to CSV</li>
</ul>
<i class="fa fa-cube fa-lg" aria-hidden="true"></i> <b>Models</b>
<ul>
	<li>View models from WoW in your browser</li>
	<li>Supports animations and skins</li>
</ul>
<i class="fa fa-map-o fa-lg" aria-hidden="true"></i> <b>Maps</b>
<ul>
	<li>Look at compiled minimaps for WoW maps</li>
	<li>Has versions from pre-alpha (0.x) to today</li>
</ul>
<i class="fa fa-hdd-o fa-lg" aria-hidden="true"></i> Builds</a>
<ul>
	<li>List of all builds from WoW patch 6.0 and up (older versions are planned to be added)</li>
	<li>Use diffs to generate a list of all differences in the files between versions</li>
	<li>All builds listed as complete are fully backed up as to not be lost to the times like many games have been before</li>
</ul>
